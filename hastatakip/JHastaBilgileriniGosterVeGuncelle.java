    private void FileGuncelle(String filePath, String oldString, String newString){
    
        File fileToBeModified = new File(filePath);
        String oldContent = "";
        BufferedReader reader = null;
        FileWriter writer = null;
        try
        {
            reader = new BufferedReader(new FileReader(fileToBeModified));
            //Reading all the lines of input text file into oldContent
            String line = reader.readLine();
            while (line != null) 
            {
                oldContent = oldContent + line + System.lineSeparator();
                line = reader.readLine();
            }
             
            //Replacing oldString with newString in the oldContent
            String newContent = oldContent.replaceAll(oldString, newString);
            System.out.println("\n^^^^^ DEGISIM YAPILDI ^^^^^");
            System.out.println("%"+oldString+"%");
            System.out.println("%"+newString+"%");
            //Rewriting the input text file with newContent
            writer = new FileWriter(fileToBeModified);
            writer.write(newContent);
        }
        catch (IOException e)
        { e.printStackTrace();}
        finally
        {try
            {
                //Closing the resources
                reader.close();
                writer.close();
            } 
            catch (IOException e) 
            {e.printStackTrace();}
        }
                
    }
        
    
    private String HastaninDogumTarihiniBul(String TcNosu) throws FileNotFoundException, IOException{
        
        String[] normList;
        String[] HDTlist = new String[0];

        File f1=new File("hastaBilgileriDogumTarihiIle.txt"); //Creation of File Descriptor for input file
        FileReader fr = new FileReader(f1);  //Creation of File Reader object
        BufferedReader br = new BufferedReader(fr); //Creation of BufferedReader object
        String s;     
        int count=0;   //Intialize the word to zero
        
        while((s=br.readLine())!=null)   //Reading Content from the file
        {    
            normList=s.split(" / ");  //Split the word using space
            
            for (String word : normList) 
            {
                if (word.equals(TcNosu))   //Search for the given word
                {
                    count++;    //If Present increase the count by one
                    HDTlist=s.split(" / ");  //Split the word using space
                }
            }
        }
        
        if(HDTlist == null){
            System.out.println("TC bulunamadi.");
        }
        
        fr.close();       
        System.out.println("\nDT BILGILERI.");
        System.out.println(HDTlist);
        return HDTlist[2];
                
    }
    
    private String[] HastaBilgileriniBul(String TcNosu) throws FileNotFoundException, IOException{
        
        String[] normalList;
        String[] HBlist = new String[0];

        File f1=new File("hastaBilgileri.txt"); //Creation of File Descriptor for input file
        FileReader fr = new FileReader(f1);  //Creation of File Reader object
        BufferedReader br = new BufferedReader(fr); //Creation of BufferedReader object
        String s;     
        int count=0;   //Intialize the word to zero
        
        while((s=br.readLine())!=null)   //Reading Content from the file
        {    
            normalList=s.split(" / ");  //Split the word using space
            
            for (String word : normalList) 
            {
                if (word.equals(TcNosu))   //Search for the given word
                {
                    count++;    //If Present increase the count by one
                    HBlist=s.split(" / ");  //Split the word using space
                }
            }
        }
        
        if(HBlist == null){
            System.out.println("TC bulunamadi.");
        }
        
        fr.close();        
        return HBlist;
                
    }
    
